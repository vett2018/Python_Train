"""Отсоритировать колекцию по последнему символу слова"""
lst = ["Москва", "Сочи", "Севастополь", "Санкт-Петербург", "Сахалин"]
b = sorted(lst, key= lambda x: x[-1]) # key= lambda x: x[0] - сортировка по первой букве слова
print(b)
